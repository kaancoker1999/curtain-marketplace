// Girilen inç ölçüsünü bir üst kırılıma yuvarlayıp grid fiyatını (USD) döndürür.
// Kırılım altı ölçüler ilk kırılımdan, kırılım üstü ölçüler son kırılımdan fiyatlanır.
export function gridPriceUSD(grid: PriceGrid, widthIn: number, heightIn: number): number {
  const wi = grid.widths.findIndex((w) => widthIn <= w)
  const hi = grid.heights.findIndex((h) => heightIn <= h)
  const w = wi === -1 ? grid.widths.length - 1 : wi
  const h = hi === -1 ? grid.heights.length - 1 : hi
  return grid.prices[h][w]
}
